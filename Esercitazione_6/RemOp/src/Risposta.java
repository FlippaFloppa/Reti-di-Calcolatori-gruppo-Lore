public class Risposta {

	private String nomeFile;
	private int nRighe;
	public Risposta(String nomeFile, int nRighe) {
		super();
		this.nomeFile = nomeFile;
		this.nRighe = nRighe;
	}
	@Override
	public String toString() {
		return "Risposta [nomeFile=" + nomeFile + ", nRighe=" + nRighe + "]";
	}
	
	
	
	
	
}
